const data = [];

for (let i = 1; i <= 100; i++) {
  const regionKeys = Object.keys(regions);
  const region = regionKeys[Math.floor(Math.random() * regionKeys.length)];
  const city =
    regions[region][Math.floor(Math.random() * regions[region].length)];
  const title = `${region} ${
    sampleTitles[Math.floor(Math.random() * sampleTitles.length)]
  }`;
  const description =
    sampleDescriptions[Math.floor(Math.random() * sampleDescriptions.length)];
  const hashtags = Array.from(
    { length: 6 },
    () => sampleTags[Math.floor(Math.random() * sampleTags.length)]
  );
  const date = new Date(
    2024,
    3 + Math.floor(Math.random() * 3),
    1 + Math.floor(Math.random() * 28)
  )
    .toISOString()
    .split("T")[0];
  const popularity = Math.floor(Math.random() * 100);
  const distance = (Math.random() * 20).toFixed(1);
  const imageIndex = Math.floor(Math.random() * 10) + 1;

  data.push({
    id: i,
    title,
    region,
    city,
    description,
    hashtags,
    date,
    popularity,
    distance: parseFloat(distance),
    image: `../img/travel/travel_${imageIndex}.jpg`,
  });
}
